    private void jbtnAddNewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnAddNewActionPerformed
        // TODO add your handling code here:
        if(jtxtMedDose.getText().equals("")||jtxtMedname.getText().equals("")||jtxtMedID.getText().equals("")||jtxtMedIssueDate.getText().equals("")
                ||jtxtMedExpiryDate.getText().equals("")||jtxtNumberofMeds.getText().equals(""))
        {    
            JOptionPane.showMessageDialog(null,"It is mandatory to fill all the fields","Hospital Management Systems",JOptionPane.OK_OPTION);
        }
        else
        {
            DefaultTableModel model=(DefaultTableModel)jTable1.getModel();
           model.addRow(new Object[]{jcmbMedType.getSelectedItem().toString(),jtxtMedname.getText(),jtxtMedID.getText(),jtxtMedIssueDate.getText(),
           jtxtMedExpiryDate.getText(),jtxtNumberofMeds.getText(),jtxtMedDose.getText()});    
        }
        
    }//GEN-LAST:event_jbtnAddNewActionPerformed

